function module:OnEnable()
	if not Raven.RegisterSpellList then return end
	for n, t in pairs(spellLists) do
		local c = Raven:RegisterSpellList(n, t, true)
	--	print("Registered Raven spell list", n, "with", c, "unique entries")
	end
	local _, class = UnitClass("player") -- player's class in standard language
	if class == "PRIEST" or class == "PALADIN" or class == "DRUID" or class == "SHAMAN" or class == "MAGE" or class == "WARLOCK" or class == "MONK" then
		local c = Raven:RegisterSpellList("Special Buffs", manaSpells, false)
	--	print("Registered Raven mana spells with", c, "unique entries")
	end
		local _, class = UnitClass("player") -- player's class in standard language
	if class == "HUNTER" then
		local c = Raven:RegisterSpellList("Special Buffs", hunterSpells, false)
	-- print("Registered Raven Hunter spells with", c, "unique entries")
	end
end